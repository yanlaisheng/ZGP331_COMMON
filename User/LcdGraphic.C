/*******************************************************************************
函  数: void LCD12864_BUFF_SetPixelIndex(u8 x, u8 y, u8 Color)
参  数:
返  回:	无

编  程: ZCL ZYX
日  期: 2013-09-09
********************************************************************************/
void LCD12864_BUFF_SetPixelIndex(u8 x, u8 y, u8 Color)
{
	u8  X_Byte;				//X坐标
	unsigned int   X_Bits;				//标记当前点处于x方向字节中的哪一位.
	//相应的位会被置1.


	if(y >= 64 || x >= 128)  return;		 //限幅,防止越界,很重要.

	X_Byte = x / 8;						 //计算出x方向属于哪一个字节
	X_Bits = 0x80 >> (x % 8);				 //属于字节中的第几个点

	if(Color == 0)						 //关闭点
	{
		LCD12864_BUFF[y][X_Byte] &=	~X_Bits;
	}
	else								 //开启点
	{
		LCD12864_BUFF[y][X_Byte] |= X_Bits;
	}
}


/*******************************************************************************
函  数:	u8 LCD12864_BUFF_PixGet(u8 x,u8 y)
参  数:	获取指定点的颜色值.
返  回:	0--当前点没有显示;1-当前点有显示.

编  程: ZCL ZYX
日  期: 2013-09-09
********************************************************************************/
u8 LCD12864_BUFF_GetPixelIndex(u8 x, u8 y)
{
	u8  X_Byte;				//X坐标
	unsigned int   X_Bits;				//标记当前点处于x方向字节中的哪一位.
	//相应的位会被置1.

	if(y >= 64 || x >= 128)  return 0xFF;	//限幅,防止越界,很重要.

	X_Byte = x / 8;						 //计算出x方向属于哪一个字节
	X_Bits = 0x80 >> (x % 8);				 //属于字节中的第几个点


	return LCD12864_BUFF[y][X_Byte] & X_Bits;
}

