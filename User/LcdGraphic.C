void LCD12864_BUFF_Num(u8 x, u8 y, u8 xSize, u8 ySize, long Num)
{
	int BiteLength = 0;                       //要显示的数字的位数
	int Wei[32];                              //要显示的数的每一位
	long NumTmp;								 //
	int i;									 //必须是有符号型


	if(Num >= 0)  NumTmp = Num;
	else        NumTmp = -Num;


	for(i = 0; i < 10; i++)
	{
		Wei[i] = NumTmp % 10;
		NumTmp /= 10;
		BiteLength++;

		if(NumTmp == 0)  break;
	}

	BiteLength--;

	if(xSize == 8 && ySize == 12)					 //字体是8x12大小
	{
		if(Num < 0)				                //如果是负数，先写负号。
		{
			//
			LCD_BUFF_DrawBMP(x, y, 8, 12, &BMP_Num_8x12[120]);
			for(i = BiteLength; i >= 0; i--) 		 //
				LCD_BUFF_DrawBMP(x + 8 + (BiteLength - i) * 8, y, 8, 12, \
				                 &BMP_Num_8x12[Wei[i] * 12]);
		}
		else if(Num > 0)
		{
			for(i = BiteLength; i >= 0; i--) 		 //
				LCD_BUFF_DrawBMP(x + (BiteLength - i) * 8, y, 8, 12, \
				                 &BMP_Num_8x12[Wei[i] * 12]);
		}
		else									  //Num==0
		{
			LCD_BUFF_DrawBMP(x, y, 8, 12, &BMP_Num_8x12[0]);
		}
	}
	else	if(xSize == 8 && ySize == 16)			 //其他字体
	{
		if(Num < 0)				                //如果是负数，先写负号。
		{
			//
			LCD_BUFF_DrawBMP(x, y, 8, 16, &BMP_Num_8x16[160]);
			for(i = BiteLength; i >= 0; i--) 		 //
				LCD_BUFF_DrawBMP(x + 8 + (BiteLength - i) * 8, y, 8, 16, \
				                 &BMP_Num_8x16[Wei[i] * 16]);
		}
		else if(Num > 0)
		{
			for(i = BiteLength; i >= 0; i--) 		 //
				LCD_BUFF_DrawBMP(x + (BiteLength - i) * 8, y, 8, 16, \
				                 &BMP_Num_8x16[Wei[i] * 16]);
		}
		else									  //Num==0
		{
			LCD_BUFF_DrawBMP(x, y, 8, 16, &BMP_Num_8x16[0]);
		}
	}
	else	if(xSize == 16 && ySize == 31)			 //其他字体
	{
		if(Num < 0)				                //如果是负数，先写负号。
		{
			//
			LCD_BUFF_DrawBMP(x, y, 16, 31, &BMP_Num_16x31[620]);
			for(i = BiteLength; i >= 0; i--) 		 //
				LCD_BUFF_DrawBMP(x + 16 + (BiteLength - i) * 16, y, 16, 31, \
				                 &BMP_Num_16x31[Wei[i] * 62]);
		}
		else if(Num > 0)
		{
			for(i = BiteLength; i >= 0; i--) 		 //
				LCD_BUFF_DrawBMP(x + (BiteLength - i) * 16, y, 16, 31, \
				                 &BMP_Num_16x31[Wei[i] * 62]);
		}
		else									  //Num==0
		{
			LCD_BUFF_DrawBMP(x, y, 16, 31, &BMP_Num_16x31[0]);
		}
	}
}




