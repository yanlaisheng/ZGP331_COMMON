/*******************************************************************************
函  数:	void LCD12864_BUFF_RefreshScreen(void)
功  能:	将显存里的数据刷刷新到屏幕.
参  数:	无
返  回:	无

编  程: ZCL ZYX
日  期: 2014-03-13
********************************************************************************/
void LCD12864_BUFF_RefreshScreen(void)
{
	u8 i, j;

	if(EnableCheckDataChangeFlg == 0)  return;
	//正有进程在写显存,不允许刷新屏幕
	//

	LCD12864_WriteCommand(0x36);		//扩展指令集

	for(i = 0; i < 32; i++)					//上半屏
	{
		LCD12864_WriteCommand(0x80 + i); //Y坐标
		LCD12864_WriteCommand(0x80);	//X坐标

		for(j = 0; j < 16; j++)  
			LCD12864_WriteData(LCD12864_BUFF[i][j]);
	}

	for(i = 0; i < 32; i++)					//下半屏
	{
		LCD12864_WriteCommand(0x80 + i); //Y坐标
		LCD12864_WriteCommand(0x88);	//X坐标

		for(j = 0; j < 16; j++)  
			LCD12864_WriteData(LCD12864_BUFF[i + 32][j]);
	}

	LCD12864_BUFF_RefreshLastData();	//刷新完毕屏幕,清空标志位
	//表示显存中的数据已经得到更新
}

