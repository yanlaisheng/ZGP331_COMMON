void LCD12864_DrawRect(u8 x, u8 y, u8 xSize, u8 ySize)
{
	u8 i;

	for(i = x; i < x + xSize; i++)					 //横线
	{
		LCD12864_BUFF_SetPixelIndex(i, y, 1);
		LCD12864_BUFF_SetPixelIndex(i, y + ySize - 1, 1);
	}

	for(i = y; i < y + ySize; i++)
	{
		LCD12864_BUFF_SetPixelIndex(x, i, 1);
		LCD12864_BUFF_SetPixelIndex(x + xSize - 1, i, 1);
	}
}

